// Forward declaration
struct command_t;

////////////////////////////////////////////////////////////////////////////////
/// Initialize the shell.
///
/// Must be called before any other shell functions.
///
/// @return   int, 0 if no errors, not 0 if errors.
////////////////////////////////////////////////////////////////////////////////
int sane_init();

////////////////////////////////////////////////////////////////////////////////
/// Shutdown the shell.
///
/// Should be called before the program is quit.
////////////////////////////////////////////////////////////////////////////////
void sane_shutdown();

////////////////////////////////////////////////////////////////////////////////
/// Execute the commands found in the command array.
///
/// @pre 'sane_init()' has been called.
///
/// @param   numCommands   int, number of commands in the command array.
/// @param   commands      command_t, command array.
////////////////////////////////////////////////////////////////////////////////
void sane_execute(int numCommands, struct command_t *commands);

////////////////////////////////////////////////////////////////////////////////
/// Get the shell's prompt.
///
/// @pre 'sane_init()' has been called.
///
/// @return   const char *, shell's prompt.
////////////////////////////////////////////////////////////////////////////////
const char *sane_getPrompt();
